#!/usr/bin/env node
/**
 * GMaestro CLI.
 *
 *   gmaestro setup    — interactive: write ~/.gmaestro/.env, init SQLite
 *   gmaestro dev      — start Next.js dashboard, open browser
 *   gmaestro reset    — clean SQLite back to a freshly-seeded demo state
 *   gmaestro doctor   — check env, SQLite, network, Anthropic tier
 *
 * Owned by: Session 3 (bin/*).
 */

import { Command } from "commander";
import { input, password, confirm } from "@inquirer/prompts";
import { spawn, spawnSync } from "node:child_process";
import { promises as fsp, existsSync, readFileSync, writeFileSync } from "node:fs";
import os from "node:os";
import path from "node:path";
import process from "node:process";
import open from "open";

const program = new Command();

program
  .name("gmaestro")
  .description("Local-first AI GTM team — multi-persona Claude agents over Composio")
  .version("0.1.0");

// ---------------------------------------------------------------------------
//  Paths + small env file helpers
// ---------------------------------------------------------------------------

const HOME = os.homedir();
const GMAESTRO_DIR = path.join(HOME, ".gmaestro");
const ENV_PATH = path.join(GMAESTRO_DIR, ".env");
const DB_PATH = path.join(GMAESTRO_DIR, "gmaestro.db");

interface EnvMap {
  [key: string]: string;
}

function readEnv(): EnvMap {
  if (!existsSync(ENV_PATH)) return {};
  const raw = readFileSync(ENV_PATH, "utf-8");
  const out: EnvMap = {};
  for (const line of raw.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const k = trimmed.slice(0, eq).trim();
    let v = trimmed.slice(eq + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    out[k] = v;
  }
  return out;
}

function writeEnv(env: EnvMap) {
  if (!existsSync(GMAESTRO_DIR)) {
    spawnSync("mkdir", ["-p", GMAESTRO_DIR], { stdio: "inherit" });
  }
  const lines = [
    "# GMaestro environment — generated by `gmaestro setup`.",
    "# Edit by hand only if you know what you're doing.",
    "",
  ];
  const order = [
    "ANTHROPIC_API_KEY",
    "COMPOSIO_API_KEY",
    "GMAESTRO_USER_ID",
    "GMAESTRO_BASE_URL",
    "GMAESTRO_TIER",
  ];
  const seen = new Set<string>();
  for (const k of order) {
    if (k in env) {
      lines.push(`${k}=${env[k]}`);
      seen.add(k);
    }
  }
  for (const [k, v] of Object.entries(env)) {
    if (!seen.has(k)) lines.push(`${k}=${v}`);
  }
  writeFileSync(ENV_PATH, `${lines.join("\n")}\n`, { mode: 0o600 });
}

function loadEnvIntoProcess() {
  const env = readEnv();
  for (const [k, v] of Object.entries(env)) {
    if (!(k in process.env)) process.env[k] = v;
  }
}

// ---------------------------------------------------------------------------
//  setup
// ---------------------------------------------------------------------------

program
  .command("setup")
  .description("Interactive wizard: configure API keys, init SQLite")
  .action(async () => {
    console.log("\nGMaestro setup\n──────────────");

    if (!existsSync(GMAESTRO_DIR)) {
      await fsp.mkdir(GMAESTRO_DIR, { recursive: true });
    }

    const existing = readEnv();

    const anthropicKey = await password({
      message: "Anthropic API key (sk-ant-…):",
      mask: "*",
      validate: (v) =>
        v.startsWith("sk-ant-") || v.length === 0
          ? true
          : "Expected an Anthropic key starting with sk-ant-",
    });
    const composioKey = await password({
      message: "Composio API key:",
      mask: "*",
    });
    const userId =
      existing.GMAESTRO_USER_ID ??
      (await input({ message: "Founder user id", default: "default" }));

    const next: EnvMap = {
      ...existing,
      ANTHROPIC_API_KEY: anthropicKey || existing.ANTHROPIC_API_KEY || "",
      COMPOSIO_API_KEY: composioKey || existing.COMPOSIO_API_KEY || "",
      GMAESTRO_USER_ID: userId,
      GMAESTRO_BASE_URL: existing.GMAESTRO_BASE_URL ?? "http://localhost:3000",
      GMAESTRO_TIER: existing.GMAESTRO_TIER ?? "auto",
    };
    writeEnv(next);
    console.log(`✔ wrote ${ENV_PATH}`);

    // Initialize SQLite via drizzle-kit migrate. Loads env first so the
    // migration command sees ANTHROPIC_API_KEY etc. (lib/shared/env.ts validates
    // on import in some downstream paths).
    loadEnvIntoProcess();
    const result = spawnSync("pnpm", ["db:migrate"], {
      stdio: "inherit",
      cwd: process.cwd(),
      env: { ...process.env },
    });
    if (result.status === 0) {
      console.log(`✔ SQLite ready at ${DB_PATH}`);
    } else {
      console.error("⚠ db:migrate failed — run `pnpm db:migrate` manually.");
    }

    console.log("\nNext: pnpm gmaestro dev");
    console.log(
      "Note: parallel persona fanout works best on Anthropic Tier 2+ ($40 cumulative spend).",
    );
    console.log("      Run `pnpm gmaestro doctor` to check your tier.\n");
  });

// ---------------------------------------------------------------------------
//  dev
// ---------------------------------------------------------------------------

program
  .command("dev")
  .description("Start the GMaestro dashboard at http://localhost:3000")
  .option("--no-open", "Don't auto-open the browser")
  .action(async (opts: { open: boolean }) => {
    loadEnvIntoProcess();
    const child = spawn("pnpm", ["next", "dev"], {
      stdio: "inherit",
      env: { ...process.env },
    });

    const stop = () => {
      if (!child.killed) child.kill("SIGINT");
    };
    process.on("SIGINT", stop);
    process.on("SIGTERM", stop);

    if (opts.open !== false) {
      // Delay slightly so Next has time to bind :3000.
      setTimeout(() => {
        void open("http://localhost:3000").catch(() => {
          /* ignore */
        });
      }, 1500);
    }

    child.on("exit", (code) => process.exit(code ?? 0));
  });

// ---------------------------------------------------------------------------
//  reset
// ---------------------------------------------------------------------------

program
  .command("reset")
  .description("Reset local SQLite to a clean demo-seeded state")
  .action(async () => {
    loadEnvIntoProcess();
    const ok = await confirm({
      message:
        "This wipes your local workflow state and re-seeds the demo leads (5 by default; SEED_LEAD_COUNT env var to override). Continue?",
      default: true,
    });
    if (!ok) return;

    const result = spawnSync("pnpm", ["tsx", "scripts/reset-demo.ts"], {
      stdio: "inherit",
      env: { ...process.env },
    });
    process.exit(result.status ?? 0);
  });

// ---------------------------------------------------------------------------
//  doctor
// ---------------------------------------------------------------------------

interface CheckResult {
  name: string;
  ok: boolean;
  detail?: string;
}

async function checkDns(host: string): Promise<CheckResult> {
  try {
    const dns = await import("node:dns/promises");
    await dns.lookup(host);
    return { name: `dns ${host}`, ok: true };
  } catch (err) {
    return {
      name: `dns ${host}`,
      ok: false,
      detail: err instanceof Error ? err.message : String(err),
    };
  }
}

async function checkAnthropicTier(
  apiKey: string,
): Promise<{ tier: "tier1" | "tier2plus" | "unknown"; limit?: number }> {
  if (!apiKey) return { tier: "unknown" };
  try {
    const res = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "x-api-key": apiKey,
        "anthropic-version": "2023-06-01",
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "claude-haiku-4-5-20251001",
        max_tokens: 1,
        messages: [{ role: "user", content: "hi" }],
      }),
    });
    const limitHeader = res.headers.get("anthropic-ratelimit-requests-limit");
    const limit = limitHeader ? parseInt(limitHeader, 10) : NaN;
    if (Number.isFinite(limit)) {
      return { tier: limit <= 50 ? "tier1" : "tier2plus", limit };
    }
    return { tier: "unknown" };
  } catch {
    return { tier: "unknown" };
  }
}

program
  .command("doctor")
  .description("Check API keys, SQLite, network, Anthropic tier")
  .action(async () => {
    loadEnvIntoProcess();
    const env = readEnv();
    const results: CheckResult[] = [];

    // env file
    results.push({
      name: `env file ${ENV_PATH}`,
      ok: existsSync(ENV_PATH),
    });

    // required keys
    results.push({
      name: "ANTHROPIC_API_KEY",
      ok: Boolean(env.ANTHROPIC_API_KEY?.startsWith("sk-ant-")),
    });
    results.push({
      name: "COMPOSIO_API_KEY",
      ok: Boolean(env.COMPOSIO_API_KEY && env.COMPOSIO_API_KEY.length > 8),
    });

    // SQLite
    results.push({
      name: `sqlite ${DB_PATH}`,
      ok: existsSync(DB_PATH),
      detail: existsSync(DB_PATH)
        ? undefined
        : "run `pnpm db:migrate` or `pnpm gmaestro setup`",
    });

    // network
    results.push(await checkDns("api.anthropic.com"));
    results.push(await checkDns("backend.composio.dev"));

    // tier ping
    let tierResult: Awaited<ReturnType<typeof checkAnthropicTier>> | null = null;
    if (env.ANTHROPIC_API_KEY) {
      tierResult = await checkAnthropicTier(env.ANTHROPIC_API_KEY);
      results.push({
        name: "anthropic tier",
        ok: tierResult.tier !== "unknown",
        detail:
          tierResult.tier === "tier1"
            ? `Tier 1 (${tierResult.limit} RPM) — sequential dispatch recommended`
            : tierResult.tier === "tier2plus"
              ? `Tier 2+ (${tierResult.limit} RPM)`
              : "couldn't determine",
      });
    }

    // print results
    console.log("\nGMaestro doctor\n───────────────");
    for (const r of results) {
      const icon = r.ok ? "✔" : "✗";
      const detail = r.detail ? ` — ${r.detail}` : "";
      console.log(`${icon} ${r.name}${detail}`);
    }

    if (tierResult?.tier === "tier1" && env.GMAESTRO_TIER !== "tier1") {
      const accept = await confirm({
        message:
          "Set GMAESTRO_TIER=tier1 in ~/.gmaestro/.env (forces sequential dispatch)?",
        default: true,
      });
      if (accept) {
        writeEnv({ ...env, GMAESTRO_TIER: "tier1" });
        console.log("✔ wrote GMAESTRO_TIER=tier1");
      }
    }

    const failed = results.filter((r) => !r.ok).length;
    process.exit(failed === 0 ? 0 : 1);
  });

// ---------------------------------------------------------------------------
//  go
// ---------------------------------------------------------------------------

program.parseAsync(process.argv).catch((err) => {
  console.error(err);
  process.exit(1);
});
